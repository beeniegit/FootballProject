package com.beeniegit.Tester.Data;

public class 사람 {
	
	protected String 이름;
	protected int 나이;
	protected String 국가;
	protected int 키;
	protected int 체중;
	
	public 사람(String 이름, int 나이, String 국가, int 키, int 체중) {
		this.이름 = 이름;
		this.나이 = 나이;
		this.국가 = 국가;
		this.키 = 키;
		this.체중 = 체중;
	}
	

}
